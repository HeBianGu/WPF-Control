        private string GetPropertyDisplayName(object descriptor)
        {
            if (descriptor is PropertyDescriptor pd)
            {
                DisplayAttribute displayName = pd.Attributes[typeof(DisplayAttribute)] as DisplayAttribute;
                if (displayName != null)
                    return displayName.Name;
            }
            if (descriptor is PropertyInfo pi)
            {
                Object[] attributes = pi.GetCustomAttributes(typeof(DisplayAttribute), true);
                for (int i = 0; i < attributes.Length; ++i)
                {
                    DisplayAttribute displayName = attributes[i] as DisplayAttribute;
                    if (displayName != null)
                        return displayName.Name;
                }
            }
            return null;
        }
