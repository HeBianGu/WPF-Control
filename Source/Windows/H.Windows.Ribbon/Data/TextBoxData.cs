namespace H.Windows.Ribbon
{
    public class TextBoxData : ControlData
    {
        public string Text
        {
            get
            {
                return _text;
            }

            set
            {
                if (_text != value)
                {
                    _text = value;
                    RaisePropertyChanged();
                }
            }
        }
        private string _text;
    }
}
